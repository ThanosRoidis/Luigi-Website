			<h2>Biography</h2>
		<p>The events leading to Luigi's creation began in 1982, during the development of Donkey Kong,
		  where the Nintendo designer Shigeru Miyamoto had created Mario (then known as "Jumpman") hoping
		  that he would be able to recast the character in a variety of different roles in future games. 
		  Miyamoto had been inspired by the game Joust to create a game with a simultaneous two-player mode, 
		  which led to his development of the game Mario Bros. in 1983, with Luigi filling the role of Mario's 
		  brother as the second playable character. Luigi's name is said to have been inspired by a pizza 
		  parlor near Nintendo of America's headquarters in Redmond, Washington, called "Mario & Luigi's".
		  Miyamoto observed that the word ruiji means "similar" in the Japanese language, and that Luigi was
		  designed to have the same size, shape and gameplay of Mario. While Mario was originally portrayed
		  as a carpenter in Donkey Kong, the duo of Mario and Luigi in Mario Bros. were styled as Italian 
		  plumbers by Miyamoto, on the suggestion of a colleague. Software constraints of the time—similar
		  to those that gave Mario his distinctive look in Donkey Kong—meant Luigi's first appearance was restricted
		  to a simple palette swap of Mario designed to represent the second player. Graphically and in terms
		  of gameplay, the characters were completely identical, except for their color schemes. The green 
		  color scheme adopted for Luigi would remain one of his defining physical characteristics in subsequent 
		  releases. After the success of Mario Bros., Luigi was introduced to a wider audience in 1985 with the
		  release of the console game Super Mario Bros. Once again his role was restricted to a palette swap of Mario,
		  functioning as the second-player in a similar fashion to Mario Bros.. The subsequent Japan-only version
		  of Super Mario Bros. 2 in 1986 (later released in the west as Super Mario Bros.: The Lost Levels) marked
		  the beginning of Luigi's development toward becoming a more distinguished character. As with his previous
		  appearances, Luigi remained a palette swap of Mario; however, his movement was no longer identical.
		   Luigi could now jump higher and farther than his brother, at the expense of movement response and precision.
          While this version of Super Mario Bros. 2 was released in Japan, it was deemed to be too difficult for American 
         audiences at the time. In 1988, consequently, an alternative release was developed to serve as Super Mario Bros. 2 
          for western players (and later released in Japan as Super Mario USA); this version would play a key role in shaping 
         Luigi's current appearance. The game was a conversion of Yume Kōjō: Doki Doki Panic, with the graphics altered to 
           represent characters and scenes from the Mario franchise. In this release, the character of "Mama", who had the highest
         jump among the original cast, served as the template for Luigi, resulting in his taller, thinner look, combined with
        his Marioesque outfit and ubiquitous green color scheme. There were earlier appearances of Luigi being taller than
         Mario: in the 1988 Famicom Disk System game Famicom Grand Prix II: 3D Hot Rally and earlier, in a very rare 1986
        anime Super Mario Bros.: Peach-Hime Kyushutsu Dai Sakusen! (though in the anime he wore a yellow shirt and the
         color of his hat and overalls were blue). Official artwork for Super Mario Bros. 3 and Super Mario World depicts
         Luigi with this new look; but Nintendo of Japan would not adapt his artwork differences to his look in-game 
         until the 1992 game Super Mario Kart. Luigi's distinctive appearance from the Yume Kōjō: Doki Doki Panic-inspired
       version of Super Mario Bros. 2 has been used ever since, even for remakes of games in which he was originally
        a palette swap.</p>
        
        
